package com.ohgiraffers.section03.increment;

public class Application1 {
    public static void main(String[] args) {
        
        int num = 20;
        System.out.println("num = " + num);

        num++;
        System.out.println("num = " + num);

        ++num;
        System.out.println("num = " + num);

        num--;
        System.out.println("num = " + num);

        --num;
        System.out.println("num = " + num);

        int firstNum = 20;
        int result1 = firstNum++ * 3;
        
    }
}
