package com.ohgiraffers.section01.literal;

public class Application1 {

    public static void main(String[] args) {
        System.out.println(123);

        System.out.println(1.23); //숫자는 따옴표 필요 x

        System.out.println('a'); //두개 이상은 문자로 취급하지 않기 때문에 에러가 발생한다. 없어도 안되고 꼭 1글자

        System.out.println('1'); //값은 숫자여로 문자로 인식

        System.out.println("안녕하세요"); //문자열은 복수의 문자를 말한다. "" 안에 넣어줘야 함

        System.out.println("123"); //""안에 들어가면 무조건 문자열
        System.out.println("1"); // '1' 과 "1"은 엄연히 다르다.
        System.out.println(""); //문자열은 비워도 에러가 나지 않는다.






        
        }
}
