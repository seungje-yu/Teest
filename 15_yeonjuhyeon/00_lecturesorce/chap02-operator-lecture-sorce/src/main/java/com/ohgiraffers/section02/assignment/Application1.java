package com.ohgiraffers.section02.assignment;

public class Application1 {

    public static void main(String[] args){

        int num = 12;
        System.out.println("origin num = " + num);
        
        num += 3;
        System.out.println("num+3 = " + num);
        
        num -= 3;
        System.out.println("num-3 = " + num);
        
        num *= 3;
        System.out.println("num*3 = " + num);

        num /= 2;
        System.out.println("num/2 = " + num);

    }
}
